package javaProgram;

public class BreakForEX {

	public static void main(String[] args) {
		
		for(int i=1; i<=10; i++) {
			System.out.println(i);
			if(i==5) {
				break;
			}
		}
		System.out.println("***" + "\n");
		
		int j=1;
		while(j<=10) {
			System.out.println(j);
			if(j==5) {
				break;
			}
			j++;
		}
	
	
	
	
	
	
	
	
	
	
	
	
	}
}
